    <h2 class="titreobj"> Les Avantages du Domotique </h2>
    <p>
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Régulation de la température et du chauffage.jpg" alt="Image sur Régulation de la température et du chauffage" title="Régulation de la température et du chauffage" class="imagepresentation" />
    <h3 id="titresec"> Régulation de la température et du chauffage : </h3>
    <br>
    Le chauffage automatisé garantira votre confort et réduira les coûts de consommation d'énergie, 
    que vous soyez à la maison ou à l'extérieur. Vous pouvez régler des températures différentes pour chaque pièce, 
    activer le chauffage à l'avance avant votre retour à la maison ou changer votre chauffe-eau juste 
    avant votre douche matinale sans avoir besoin de garder l'eau chaude tout le temps. 
    La température peut également être régulée en fonction de la météo actuelle ou des prévisions météorologiques.
    </p>
    
    <p class="libre">
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Volets et stores.jpg" alt="Images sur Volets et stores" title="Volets et stores" class="figurepresentation" />
    <h3 id="titresec"> Volets et stores : </h3>
    <br>
    L'automatisation de l'ombrage vous réveille chaque matin à la lumière naturelle avec des stores
    pare-soleil ne laissant passer que la quantité optimale de lumière. Au crépuscule, les stores se ferment 
    automatiquement pour assurer votre intimité. La régulation passive automatique de la température peut 
    être obtenue en ouvrant les stores en hiver, si la lumière du soleil est suffisante. <br>
    Le système rétractera également les stores en cas de vent fort pour éviter tout dommage. 
    </p>
    
    <p class="libre">
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Sécurité.png" alt="Image sur Sécurité" title="Sécurité" class="imagepresentation" />
    <br>
    <h3 id="titresec"> Sécurité : </h3>
    Les systèmes domotiques sont de plus en plus sophistiqués en matière de sécurité. 
    Ils peuvent par exemple combiner des contacts de porte et de fenêtre, des détecteurs de fumée,
    de mouvement et d’humidité ainsi qu’un éclairage automatique des issues de secours. <br>
    En cas d’urgence, le système peut non seulement déclencher un signal sonore, mais il peut aussi appeler les secours ou envoyer une notification.
    En outre, il peut intégrer des caméras de surveillance dont le propriétaire peut consulter les images à distance sur Internet, 
    depuis son bureau par exemple. 
    </p>
    
    <p class="libre">
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Ventilation.jpg" alt="Images sur Ventilation" title="Ventilation" class="figurepresentation" />
    <br>
    <h3 id="titresec"> Ventilation : </h3>
    Si des concentrations élevées de dioxyde de carbone (CO 2 ) sont détectées dans une pièce, 
    la ventilation intelligente augmentera automatiquement l'apport d'air frais. L'air extérieur 
    peut être utilisé pour réguler la température intérieure. La ventilation peut être déclenchée 
    en détectant une augmentation de l'humidité (par exemple dans les salles de bain).
    </p>
    
    <p class="libre">
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Éclairage.jpg" alt="Image sur Éclairage" title="Éclairage" class="imagepresentation" />
    <br>
    <h3 id="titresec"> Éclairage : </h3>
    Plus de soucis si vous avez oublié les lumières allumées. Le système intelligent éteint 
    automatiquement toutes les lumières au moment où vous quittez votre maison. Vous pouvez 
    également contrôler des groupes d'éclairage entiers ou programmer lorsque vous souhaitez 
    allumer les lumières. En fonction de l'intensité lumineuse, le système peut également 
    désactiver temporairement l'éclairage déclenché par le mouvement pour augmenter la durée de 
    vie des sources lumineuses s'il y a suffisamment de lumière. 
    </p>
    
    <p class="libre">
    <br><br>
    <img src="./Images/Les Avantages du Domotique/Suivi énergétique.png" alt="Image sur Suivi énergétique" title="Suivi énergétique" class="figurepresentation" />
    <br>
    <h3 id="titresec"> Suivi énergétique : </h3>
    Surveillez la consommation d'énergie et son coût et le système s'occupera du reste. 
    En raison de l'interconnexion avec d'autres appareils domestiques intelligents, le système 
    économise de l'énergie en chauffant ou en refroidissant passivement, en fermant les composants 
    inutilisés lorsque vous êtes absent de la maison (c'est-à-dire en fermant les appareils en mode 
    veille, en fermant le chauffe-eau pour économiser de l'énergie, etc.) . En utilisant de tels 
    systèmes, vous pouvez réduire vos dépenses jusqu'à 50 %.
    </p>
